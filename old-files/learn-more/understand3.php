<h1>Understand Easily</h1> 
 
<p>Understanding tasks take up a significant portion of a developers time. Architexa allows developers to more quickly decipher the code they need to work with.</p> 
<b>Create dynamic digrams that will help with the following:</b>
<ul> 
<li>Large codebases often take a correspondingly large amount of time to understand.</li>
	
<li>New projects and libraries can contain unfamiliar structures and design patterns that take time to unravel.</li> 
<li>Complex or confusing code takes time to decipher; whether it was written by other developers or yourself six months ago.</li> 
</ul> 
 
<p>Proper documentation, developer training, and a consistent architecture can all help lower the amount of time developers spend on understanding. Architexa's dynamic diagrams help improve efficiency in each of these areas so that the time speant on understanding is significantly reduced.</p> 
 
