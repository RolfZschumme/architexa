<p>Want hassle-free code reviews? Architexa helps you create 
code diagrams which makes it much easier to discuss the code with rest of the 
team. Team members can see and understand aspects of the code that may not have 
caught their attention earlier. Such support allows for productive discussions 
around improving and refactoring existing code and leads to architecture 
improvements that teams can use to easily add unique customer-oriented 
'innovative' features.</p>	

