<h1>Code Understanding is a Large Problem</h1>
<p style="margin:-10px 0pt 5pt 0px;">Whether it is understanding complex code, understanding code that was written by someone who just left the team, understanding new code when moving onto a new project, or just understanding code that was written by oneself just 3-4 months ago - making sense of code has been known to be a big challenge for software developers. There have been a number of studies that have been conducted by researchers studying developers' coding needs. Below are some of the major results.</p>
