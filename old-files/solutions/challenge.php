<h1>The Challenge For Developers - Understanding Code</h1>

<p>In study after study the results have been consistent: <b>programmers spend a 
large amount of their time making sense of code.</b> This is true whether the code was written by other team members or by the programmer himself just 4 months 
ago.</p>

