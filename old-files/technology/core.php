<h2>Architexa's Rich Source Exploration Tool Suite is the first suite to:</h2>
<ul>
<li><b><i>let you create intuitive diagrams straight from the code</i></b>, helping you understand fast. 
Create a layered diagram to get a mile-high view of the project architecture, use a class diagram to understand the interactions between the classes, and follow the control 
flow or analyze logic at a fine-grained level using a sequence diagram.
</li>

<li><b><i>deeply integrate with the IDE</i></b> to create diagrams. IDEs are 
an integral part of any developer's work, and Architexa lets developers create 
diagrams from the stack trace, from open java editor tabs, or from a project's uncommitted changes.  </li>

<li><b><i>let you easily control what to show in the diagram</i></b>. Diagrams created by UML tools can easily get overwhelming. Architexa helps to manage these diagrams.<br>
- Remove or add components to the diagram with ease.<br> 
- Don't want that conditional block? <i>collapse it!</i><br>
- Don't need the package/class? <i>delete it!</i>   
</li>

<li><b><i>share code concepts with just a single-click</i></b>.  Architexa 
allows developers to create a diagram and share it with teammates sitting 
across the hall or across the globe by using the one-click upload feature.
</li>
</ul>

