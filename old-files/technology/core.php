<h1>Rich Source Exploration</h1>


<p>Architexa's approach provides tools that allow developers to richly explore their source code. <b>Rich Source Exploration helps programmers intuitively understand code.</b><br>

<br>Developers can create diagrams that are familiar and specific to their tasks. This offers many useful scenarios to the user:
<ul>
<li>Interactively explore new code through high-level and detailed structural views.</li>
<li>See multiple classes, methods, and fields in one place.</li>
<li>Simplify code reviews through shared design diagrams.</li>
<li>Speed-up the ramping of new programmers.</li>
<li>Quickly comprehend logic by following relations and control flow in a diagram.</li>
<li>Re-architect your code and confirm the correct design in a diagram.</li>

</ul></p>

