<h1>Where Tools Have Failed</h1>


<p>The challenge when using most tools has been that they are hard to use, 
almost as hard as comprehending the code. Tools have traditionally focused either on the needs of designers and modellers by providing significant support for generating code from UML diagrams or on the needs of software architects by providing powerful technology that requires significant training.</p>

<p>Architexa provides a suite that supports an intuitive but powerful interface to UML diagramming by giving users an interactive exploratory visualization platform.</p>



