<h1>Architexa Open Source Initiative</h1>

<p>
At Architexa we want to help developers easily understand and document code. One particular area that we care about is the needs of the Open Source community.</p>
<p>
We often hear about how the documentation and understanding challenges in OSS projects make the softwares unusable for other developers. We believe we can help bring a clear and transparent architecture to projects, so that the code will not deteriorate, and users of such projects will be able to integrate them more effectively into their projects.</p>

<p>
In our efforts to help OSS developers, we are building <strong><a href="http://www.codemaps.org">CodeMaps</a></strong>, a free site that enables community-developed architectural documentation to be readily accessible.
</p>


<p>
But we are going beyond that, if you contribute to an Open Source Project, we will give you free licenses to our <a href="http://www.architexa.com/learn-more/index ">downloadable tools</a> as well - so that you can benefit from our deep IDE integration.
</p>

<p>
Join <a href="http://www.codemaps.org">CodeMaps</a> now and help us improve it by sending your feedback.
</p>
