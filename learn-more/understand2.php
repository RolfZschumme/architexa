<h1>Understand complex codebases</h1> 
 
<p>It is often difficult to decipher code no matter whether you are joining a new project, using a new library, or even looking at the code you wrote six months ago. Using Architexa to dynamically create diagrams from your code can help solve this problem by making it simple and fast to explore code and determine how it interacts with the rest of the codebase.</p> 
 
<p>Team members that are new to a project, company, or codebase can get up to speed fastest when being directly tutored by someone who is well versed in the codebase. This often takes a lot of time away from development and may not be possible on a distributed team. By utilizing the Architexa diagramming engine it is easy for team members to explore new code visually while at the same time investigating the code in a traditional editor.</p> 
 
