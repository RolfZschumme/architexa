<h1>
Challenges following Agile Principles – Collaboration and Time constraint
</h1>

<p>
Agile principles are largely helpful to software development project. However, many developers find it highly 
challenging to apply these Agile principles in practice. In this section, we shall 
discuss why developers find it difficult to follow Agile principles.
</p>

<h2>
Collaboration Challenges 
</h2>

<p>
Agile, with an iterative approach, emphasizes tight collaboration among different developer teams. This leads to higher 
communication overhead and makes it important for everyone to be on the same page. 
</p>

<p>
Unfortunately, the Agile principle of coding-first often produces lesser documentation and architectural 
descriptions. This poses a significant challenge as developers need to communicate more but are 
unable to do so without common point of references found in diagrams and documentations. 
</p>

<h2>
Responding to changes
</h2>
<p>
Another key challenge of Agile is responding to changing requirement and business environment. Without a clear 
overview of the software architecture, developers find it hard to identify the structural changes that is required
to meet the changing business requirement. 
</p>

