<p>A picture is worth a thousand words! But are some pictures better than others?</p>

<p>What if you could right click on the code you're looking at and 
demand to see it in a diagram? What if you could easily explore relationships 
in that diagram and drag-and drop code to build a picture that you care 
about? Imagine a tool that gives you this power and also helps you keep the diagram under
control.</p>

<p>Architexa lets you create diagrams that work as an intelligent sketchpad. If you do something often on a piece of paper and there is a way to make it easier to do on a computer, we want to support it.</p>



<p>Architexa's Rich Source Explorer provides developers with a highly-interactive interface to understand and collaborate with code.</p>




<h1>Understand with Familiar Diagrams</h1>

<p>Take advantage of our support for the most common types of diagrams created 
