<h1> Documentation </h1>
<p>We at Architexa believe in the saying <b>“Pictures speak louder than words”</b>. Thus we have put the fun back into documentation through pictures which show a simplified, easy-to-read version of UML to help you understand code. Architexa helps:
</p>

<h2>Save Time </h2>
<ul type="square">
<li>Having good documentation is often time consuming.</li>
<li>Developers feel that often there is no need to document as they know the code in and out and feel that their code is simple. </li>
<li>Our product deals with this problem by helping the developer to come up with  a high level documentation while generating the diagrams and adding notes next to it while making changes</li>
</ul>
<h2>New developers up to speed</h2>
<p>The documentation process helps new developers understand the entire code structure easily thus helping them take lesser time to adapt and increase their coding speed.</p>
<h2>Involving other team members</h2>
<p>With the help of documentation the members of other teams can exactly understand as to what’s happening in the architecture and continue the coding from the point where the previous developer left.</p>
<h2>Improving code quality with time</h2>
<p>Documentation helps in better understanding of the code which will make developers continue with the project or refer to it as needed thus helping them improve their code quality.</p>
<h2>Refresh quickly</h2>
<p>Developers can easily refresh the feature implementation of old and completed projects as all the high level documentation gets saved on the Architexa server.</p>
<h2>Diagram Connectivity</h2>
<p>Architexa’s tool allows the developers to easily find the diagrams related with each other with respect to the classes. Thus no need for the developer to find each class separately. Thus saving a lot of time for the developer </p>
<h2>Comments on the server</h2>
<p>Architexa allows you to save the diagrams related to your code on its server thus making it very easy for any of your team member to access the diagram whenever they want. Even all the notes that either you or your team member add to the diagrams gets saved on the serve which helps in documenting and better understanding of the code.</p>
<h2>Let anyone build documentation</h2>
<p>Most developers unfortunately have the false feeling that their code is already well documented. Documentation therefore often needs to be started by non-developers or those working on other parts of the code. Architexa allows for a lightweight low-commitment approach for letting code users build the documentation. Anyone can improve documentation and the popular diagrams can be reviewed by the core-developers for accuracy before included as part of the official documentation</p>


